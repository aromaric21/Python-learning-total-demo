# Programme pour calculer le volume d'un parallélépipède rectangle

# Saisie des dimensions
largeur = float(input("Entrez la largeur : "))
hauteur = float(input("Entrez la hauteur : "))
profondeur = float(input("Entrez la profondeur : "))

# Calcul du volume
volume = largeur * hauteur * profondeur

# Affichage du résultat
print(f"Le volume du parallélépipède rectangle est : {volume}")